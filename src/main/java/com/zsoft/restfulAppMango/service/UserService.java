package com.zsoft.restfulAppMango.service;

import com.zsoft.restfulAppMango.domaine.Authority;
import com.zsoft.restfulAppMango.domaine.User;
import com.zsoft.restfulAppMango.Repositories.AuthorityRepository;
import com.zsoft.restfulAppMango.Repositories.UserRepository;
import com.zsoft.restfulAppMango.security.AuthoritiesConstants;
import com.zsoft.restfulAppMango.service.UserDTO;
import org.slf4j.Logger;
import org.slf4j.LoggerFactory;
